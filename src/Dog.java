public class Dog {
    private String name;
    private int age;
    private String color;
    private String WhatCanHeDo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getWhatCanHeDo() {
        return WhatCanHeDo;
    }

    public void setWhatCanHeDo(String whatCanHeDo) {
        WhatCanHeDo = whatCanHeDo;
    }
}
